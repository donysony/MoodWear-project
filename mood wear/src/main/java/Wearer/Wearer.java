package Wearer;

public class Wearer {
	
	private String wearer_Btitle;
	private String wearer_Stitle;
	private String wearer_content;
	private String wearer_img;
	
	public String getWearer_Btitle() {
		return wearer_Btitle;
	}
	public void setWearer_Btitle(String wearer_Btitle) {
		this.wearer_Btitle = wearer_Btitle;
	}
	public String getWearer_Stitle() {
		return wearer_Stitle;
	}
	public void setWearer_Stitle(String wearer_Stitle) {
		this.wearer_Stitle = wearer_Stitle;
	}
	public String getWearer_content() {
		return wearer_content;
	}
	public void setWearer_content(String wearer_content) {
		this.wearer_content = wearer_content;
	}
	public String getWearer_img() {
		return wearer_img;
	}
	public void setWearer_img(String wearer_img) {
		this.wearer_img = wearer_img;
	}
}
